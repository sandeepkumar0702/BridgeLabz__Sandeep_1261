let age = 18;

if (age < 18) {
    console.log('You are underage');
} else if (age === 18) {
    console.log('You are exactly 18');
} else {
    console.log('You are an adult');
}

let num = 10;

if (num > 0) {
    if (num % 2 === 0) {
        console.log("Positive and even");
    } else {
        console.log("Positive but odd");
    }
} else {
    console.log("Not a positive number");
}
